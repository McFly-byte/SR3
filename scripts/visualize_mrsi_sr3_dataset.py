#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Visualize offline MRSI SR3 dataset samples.

This script reads a split directory containing ``manifest.csv`` and
``samples/*.npz`` generated by ``data/prepare_mrsi_sr3_pairs.py``. It produces
per-sample diagnostic panels for checking:

- LR/HR DMI pair quality
- k-space degradation strength
- T1/FLAIR alignment with the DMI mask
- mask coverage and basic ROI statistics
- metabolite label / patient / slice metadata

Example:
    python scripts/visualize_mrsi_sr3_dataset.py ^
      --dataset-root dataset_mrsi/mrsi_sr3_64 ^
      --split train ^
      --num-samples 12 ^
      --out-dir dataset_mrsi/mrsi_sr3_64/visual_check/train
"""

from __future__ import annotations

import argparse
import csv
import json
import random
from pathlib import Path
from typing import Dict, Iterable, List, Optional, Sequence, Tuple

import matplotlib.pyplot as plt
import numpy as np


MET_ID_TO_NAME = {0: "Glx", 1: "Glc", 2: "Lac", 3: "Lipid"}


def _parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Visualize manifest.csv + npz samples from an MRSI SR3 dataset split."
    )
    parser.add_argument(
        "--dataset-root",
        type=str,
        default="dataset_mrsi/mrsi_sr3_64",
        help="Dataset root containing train/val/test split folders.",
    )
    parser.add_argument(
        "--split",
        type=str,
        default="train",
        choices=("train", "val", "test"),
        help="Split to visualize.",
    )
    parser.add_argument(
        "--manifest",
        type=str,
        default=None,
        help="Optional explicit manifest.csv path. Overrides --dataset-root/--split.",
    )
    parser.add_argument(
        "--out-dir",
        type=str,
        default=None,
        help="Output directory. Default: <dataset-root>/visual_check/<split>.",
    )
    parser.add_argument(
        "--num-samples",
        type=int,
        default=12,
        help="Number of samples to visualize when --indices is not provided.",
    )
    parser.add_argument(
        "--indices",
        nargs="*",
        type=int,
        default=None,
        help="Manifest row indices to visualize. If set, random sampling is disabled.",
    )
    parser.add_argument(
        "--patients",
        nargs="*",
        type=int,
        default=None,
        help="Optional patient_id filter.",
    )
    parser.add_argument(
        "--metabolites",
        nargs="*",
        type=str,
        default=None,
        help="Optional metabolite filter, e.g. Glc Lac.",
    )
    parser.add_argument(
        "--seed",
        type=int,
        default=0,
        help="Random seed for sample selection.",
    )
    parser.add_argument(
        "--first",
        action="store_true",
        help="Use the first N filtered rows instead of random rows.",
    )
    parser.add_argument(
        "--cmap",
        type=str,
        default="turbo",
        help="Matplotlib colormap for DMI maps.",
    )
    parser.add_argument(
        "--struct-cmap",
        type=str,
        default="gray",
        help="Matplotlib colormap for T1/FLAIR/mask.",
    )
    parser.add_argument(
        "--dpi",
        type=float,
        default=150.0,
        help="Output PNG DPI.",
    )
    parser.add_argument(
        "--grid",
        action="store_true",
        help="Also save a compact overview grid for all selected samples.",
    )
    parser.add_argument(
        "--max-grid-samples",
        type=int,
        default=24,
        help="Maximum samples in the optional overview grid.",
    )
    return parser.parse_args()


def _read_manifest(manifest_path: Path) -> List[Dict[str, str]]:
    with manifest_path.open("r", encoding="utf-8") as f:
        return list(csv.DictReader(f))


def _split_root(args: argparse.Namespace) -> Tuple[Path, Path]:
    if args.manifest:
        manifest = Path(args.manifest)
        return manifest.parent, manifest
    split_root = Path(args.dataset_root) / args.split
    return split_root, split_root / "manifest.csv"


def _met_name(row: Dict[str, str], data: Optional[np.lib.npyio.NpzFile] = None) -> str:
    if row.get("met_name"):
        return str(row["met_name"])
    if data is not None and "met_id" in data.files:
        return MET_ID_TO_NAME.get(int(data["met_id"]), str(int(data["met_id"])))
    if row.get("met_id"):
        return MET_ID_TO_NAME.get(int(row["met_id"]), str(row["met_id"]))
    return "unknown"


def _filter_rows(
    rows: List[Dict[str, str]],
    patients: Optional[Sequence[int]],
    metabolites: Optional[Sequence[str]],
) -> List[Tuple[int, Dict[str, str]]]:
    patient_set = None if not patients else {int(x) for x in patients}
    met_set = None if not metabolites else {str(x).lower() for x in metabolites}
    out = []
    for idx, row in enumerate(rows):
        if patient_set is not None and int(row.get("patient_id", -999999)) not in patient_set:
            continue
        if met_set is not None and str(row.get("met_name", "")).lower() not in met_set:
            continue
        out.append((idx, row))
    return out


def _select_rows(
    indexed_rows: List[Tuple[int, Dict[str, str]]],
    *,
    indices: Optional[Sequence[int]],
    num_samples: int,
    seed: int,
    first: bool,
) -> List[Tuple[int, Dict[str, str]]]:
    if indices:
        wanted = {int(x) for x in indices}
        selected = [item for item in indexed_rows if item[0] in wanted]
        missing = sorted(wanted - {idx for idx, _ in selected})
        if missing:
            print(f"Warning: requested indices not found after filters: {missing}")
        return selected
    if first:
        return indexed_rows[: max(0, int(num_samples))]
    rng = random.Random(int(seed))
    n = min(max(0, int(num_samples)), len(indexed_rows))
    return rng.sample(indexed_rows, n) if n < len(indexed_rows) else list(indexed_rows)


def _load_sample(split_root: Path, row: Dict[str, str]) -> np.lib.npyio.NpzFile:
    rel = row.get("npz")
    if not rel:
        raise ValueError("manifest row is missing 'npz'")
    path = split_root / rel
    if not path.exists():
        raise FileNotFoundError(f"npz not found: {path}")
    return np.load(str(path))


def _squeeze_map(data: np.lib.npyio.NpzFile, key: str) -> np.ndarray:
    arr = np.asarray(data[key], dtype=np.float32).squeeze()
    if arr.ndim != 2:
        raise ValueError(f"{key} must be 2D after squeeze, got shape {arr.shape}")
    return arr


def _safe_range(*arrays: np.ndarray) -> Tuple[float, float]:
    vals = np.concatenate([np.asarray(a, dtype=np.float32).reshape(-1) for a in arrays])
    vals = vals[np.isfinite(vals)]
    if vals.size == 0:
        return 0.0, 1.0
    vmin = float(np.min(vals))
    vmax = float(np.max(vals))
    if np.isclose(vmin, vmax):
        vmax = vmin + 1e-6
    return vmin, vmax


def _masked_stats(x: np.ndarray, mask: np.ndarray) -> Dict[str, float]:
    m = mask > 0.5
    vals = x[m] if np.any(m) else x.reshape(-1)
    return {
        "mean": float(np.mean(vals)),
        "std": float(np.std(vals)),
        "min": float(np.min(vals)),
        "max": float(np.max(vals)),
        "sum": float(np.sum(vals)),
        "mask_ratio": float(np.mean(mask > 0.5)),
    }


def _add_mask_contour(ax, mask: np.ndarray) -> None:
    if np.any(mask > 0.5):
        ax.contour(mask > 0.5, levels=[0.5], colors="white", linewidths=0.7)


def _plot_image(ax, img: np.ndarray, title: str, cmap: str, vmin=None, vmax=None, colorbar: bool = False) -> None:
    im = ax.imshow(img, cmap=cmap, vmin=vmin, vmax=vmax)
    ax.set_title(title, fontsize=9)
    ax.set_xticks([])
    ax.set_yticks([])
    if colorbar:
        plt.colorbar(im, ax=ax, fraction=0.046, pad=0.04)


def _plot_profiles(ax, lr: np.ndarray, hr: np.ndarray, mask: np.ndarray) -> None:
    m = mask > 0.5
    if np.any(m):
        rows = np.where(m.any(axis=1))[0]
        cols = np.where(m.any(axis=0))[0]
        y = int(rows[len(rows) // 2])
        x = int(cols[len(cols) // 2])
    else:
        y = lr.shape[0] // 2
        x = lr.shape[1] // 2
    ax.plot(hr[y, :], label=f"HR row {y}", linewidth=1.4)
    ax.plot(lr[y, :], label=f"LR row {y}", linewidth=1.2)
    ax.plot(hr[:, x], label=f"HR col {x}", linewidth=1.0, alpha=0.75)
    ax.plot(lr[:, x], label=f"LR col {x}", linewidth=1.0, alpha=0.75)
    ax.set_title("Center profiles", fontsize=9)
    ax.set_ylim(bottom=0.0)
    ax.grid(alpha=0.2)
    ax.legend(fontsize=6, loc="upper right")


def _visualize_one(
    split_root: Path,
    out_dir: Path,
    row_index: int,
    row: Dict[str, str],
    *,
    cmap: str,
    struct_cmap: str,
    dpi: float,
) -> Dict[str, object]:
    data = _load_sample(split_root, row)
    hr = _squeeze_map(data, "hr")
    lr = _squeeze_map(data, "lr")
    t1 = _squeeze_map(data, "t1")
    flair = _squeeze_map(data, "flair")
    mask = _squeeze_map(data, "mask")
    err = np.abs(hr - lr)
    vmin, vmax = _safe_range(hr, lr)
    evmin, evmax = _safe_range(err)

    patient_id = int(data["patient_id"]) if "patient_id" in data.files else int(row.get("patient_id", -1))
    slice_idx = int(data["slice_idx"]) if "slice_idx" in data.files else int(row.get("slice_idx", -1))
    met_id = int(data["met_id"]) if "met_id" in data.files else int(row.get("met_id", -1))
    lowres = int(data["lowres"]) if "lowres" in data.files else int(row.get("lowres", -1))
    sample_id = int(row.get("sample_id", row_index))
    met_name = _met_name(row, data)
    stats_hr = _masked_stats(hr, mask)
    stats_lr = _masked_stats(lr, mask)

    fig, axes = plt.subplots(2, 4, figsize=(14, 7), constrained_layout=True)
    _plot_image(axes[0, 0], lr, "LR-DMI", cmap, vmin, vmax, colorbar=True)
    _add_mask_contour(axes[0, 0], mask)
    _plot_image(axes[0, 1], hr, "HR-DMI", cmap, vmin, vmax, colorbar=True)
    _add_mask_contour(axes[0, 1], mask)
    _plot_image(axes[0, 2], err, "|HR - LR|", cmap, evmin, evmax, colorbar=True)
    _add_mask_contour(axes[0, 2], mask)
    _plot_image(axes[0, 3], mask, "Mask", struct_cmap, 0.0, 1.0)

    _plot_image(axes[1, 0], t1, "T1", struct_cmap, 0.0, 1.0)
    _add_mask_contour(axes[1, 0], mask)
    _plot_image(axes[1, 1], flair, "FLAIR", struct_cmap, 0.0, 1.0)
    _add_mask_contour(axes[1, 1], mask)
    _plot_image(axes[1, 2], hr * (mask > 0.5), "HR within mask", cmap, vmin, vmax, colorbar=True)
    _add_mask_contour(axes[1, 2], mask)
    _plot_profiles(axes[1, 3], lr, hr, mask)

    fig.suptitle(
        f"row={row_index} sample={sample_id} patient={patient_id} slice={slice_idx} "
        f"met={met_name}({met_id}) lowres_half={lowres} | "
        f"HR mean={stats_hr['mean']:.4f}, LR mean={stats_lr['mean']:.4f}, mask={stats_hr['mask_ratio']:.3f}",
        fontsize=10,
    )

    out_name = f"row{row_index:06d}_sample{sample_id}_pid{patient_id}_slice{slice_idx}_met{met_name}.png"
    out_path = out_dir / out_name
    fig.savefig(out_path, dpi=float(dpi))
    plt.close(fig)

    return {
        "row_index": row_index,
        "sample_id": sample_id,
        "patient_id": patient_id,
        "slice_idx": slice_idx,
        "met_id": met_id,
        "met_name": met_name,
        "lowres": lowres,
        "mask_ratio": stats_hr["mask_ratio"],
        "hr_mean": stats_hr["mean"],
        "hr_std": stats_hr["std"],
        "hr_max": stats_hr["max"],
        "lr_mean": stats_lr["mean"],
        "lr_std": stats_lr["std"],
        "lr_max": stats_lr["max"],
        "mean_abs_hr_lr": float(np.mean(err[mask > 0.5]) if np.any(mask > 0.5) else np.mean(err)),
        "figure": str(out_path),
    }


def _write_summary_csv(rows: List[Dict[str, object]], out_path: Path) -> None:
    if not rows:
        return
    with out_path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=list(rows[0].keys()))
        writer.writeheader()
        writer.writerows(rows)


def _save_overview_grid(
    split_root: Path,
    selected: Sequence[Tuple[int, Dict[str, str]]],
    out_path: Path,
    *,
    cmap: str,
    struct_cmap: str,
    max_samples: int,
    dpi: float,
) -> None:
    selected = list(selected)[: max(1, int(max_samples))]
    if not selected:
        return
    n = len(selected)
    fig, axes = plt.subplots(n, 5, figsize=(13, max(2.2, 2.1 * n)), constrained_layout=True)
    if n == 1:
        axes = np.expand_dims(axes, axis=0)
    for r, (row_index, row) in enumerate(selected):
        data = _load_sample(split_root, row)
        hr = _squeeze_map(data, "hr")
        lr = _squeeze_map(data, "lr")
        t1 = _squeeze_map(data, "t1")
        flair = _squeeze_map(data, "flair")
        mask = _squeeze_map(data, "mask")
        err = np.abs(hr - lr)
        vmin, vmax = _safe_range(hr, lr)
        titles = [
            f"LR\nrow {row_index}",
            f"HR\n{_met_name(row, data)}",
            "|HR-LR|",
            f"T1\npid {int(data['patient_id'])}",
            "FLAIR",
        ]
        images = [lr, hr, err, t1, flair]
        cmaps = [cmap, cmap, cmap, struct_cmap, struct_cmap]
        for c, img in enumerate(images):
            _plot_image(axes[r, c], img, titles[c], cmaps[c], *( _safe_range(img) if c == 2 else (vmin, vmax) if c < 2 else (0.0, 1.0)))
            _add_mask_contour(axes[r, c], mask)
    fig.savefig(out_path, dpi=float(dpi))
    plt.close(fig)


def main() -> None:
    args = _parse_args()
    split_root, manifest_path = _split_root(args)
    if not manifest_path.exists():
        raise FileNotFoundError(f"manifest.csv not found: {manifest_path}")
    out_dir = Path(args.out_dir) if args.out_dir else Path(args.dataset_root) / "visual_check" / args.split
    out_dir.mkdir(parents=True, exist_ok=True)

    rows = _read_manifest(manifest_path)
    filtered = _filter_rows(rows, patients=args.patients, metabolites=args.metabolites)
    selected = _select_rows(
        filtered,
        indices=args.indices,
        num_samples=args.num_samples,
        seed=args.seed,
        first=bool(args.first),
    )
    if not selected:
        raise ValueError("No samples selected. Check filters, split, or indices.")

    summaries = []
    for row_index, row in selected:
        summaries.append(
            _visualize_one(
                split_root,
                out_dir,
                row_index,
                row,
                cmap=args.cmap,
                struct_cmap=args.struct_cmap,
                dpi=float(args.dpi),
            )
        )

    _write_summary_csv(summaries, out_dir / "summary.csv")
    (out_dir / "selection.json").write_text(
        json.dumps(
            {
                "manifest": str(manifest_path),
                "split_root": str(split_root),
                "selected_row_indices": [idx for idx, _ in selected],
                "num_selected": len(selected),
            },
            ensure_ascii=False,
            indent=2,
        ),
        encoding="utf-8",
    )
    if args.grid:
        _save_overview_grid(
            split_root,
            selected,
            out_dir / "overview_grid.png",
            cmap=args.cmap,
            struct_cmap=args.struct_cmap,
            max_samples=int(args.max_grid_samples),
            dpi=float(args.dpi),
        )

    print(f"Saved {len(summaries)} sample panels to: {out_dir}")
    print(f"Summary CSV: {out_dir / 'summary.csv'}")
    if args.grid:
        print(f"Overview grid: {out_dir / 'overview_grid.png'}")


if __name__ == "__main__":
    main()
